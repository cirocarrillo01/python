# operadores aritmeticos

a = 10
b = 3

# suma +
suma = a + b
print(f'Suma: {suma}')

# resta -
resta = a - b
print(f'Resta: {resta}')

# multiplicacion *
multiplicacion = a * b
print(f'Multiplicacion: {multiplicacion}')

# division /(retorna untiplo float)
division = a/b
print(f'Division: {division}')

# division entera (//)
division_entera = a // b
print(f'Division entera: {division_entera}')

# modulo (%) residuo de la division
modulo = a % b
print(f'Residuo de la division: {modulo}')

# exponente (potencia )**
exponente = a ** b # 10 elevado a la 3 = 10*10*10=100
print(f'exponente: {exponente}')
