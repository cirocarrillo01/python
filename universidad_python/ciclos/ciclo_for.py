print('----- ciclo for -----')
print('recorremos los caracteres de la cadena:')
cadena = 'Hola Mundo'
#iteramos las caracteres
for letra in cadena:
    print(letra, end=' ')

print('\n\nrecorremos la lista de frutas:')
frutas = ['platano', 'fresa', 'mango']
for fruta in frutas:
    print(fruta, end=' ')
