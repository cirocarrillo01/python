#------ operador or ------
#si uno o las dos condiciones es verdadera la operacion es verdadero

#True or False = True
#False or True = True
#True or True = True
#False or False = False

print('----- operador logio or ------')
condicion1 = False
condicion2 = True
# El operador or regresa True si cualquiera de los operadores es True
resultado = condicion1 or condicion2
print(f'Resultado {condicion1} or {condicion2} es: {resultado}')


