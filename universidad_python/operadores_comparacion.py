# operadores de comparacion
print('operadores de comparacion')
a,b = 5, 5
print(f'valor inicial a: {a}, b: {b}')

# operador igualdad ==
resultado = a == b
print(f'resultado a == b es: {resultado}')

# operador diferente !=
resultado = a != b
print(f'resultado a != b es: {resultado}')

# operador mayor que >
resultado = a > b
print(f'resultado a > b es: {resultado}')

# operador mayor o igual que >=
resultado = a >= b
print(f'Resultado a >= b es: {resultado}')

# operador menor que
resultado = a < b
print(f'resultado a <  b es: {resultado}')

# operador menor o igual que
resultado = a <= b
print(f'Resultado a <= b es: {resultado}')

