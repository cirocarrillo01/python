# metodos de cadenas

cadena1 = 'hola mundo'
print(f'cadena original: {cadena1}')
mayusculas = cadena1.upper()# para convertir a mayusculas
print(f'cadena en mayusculas: {mayusculas}')
print(f'cadena en minusculas: {cadena1.lower()}')#convertir a minusculas
cadena2 = ' juan perez '
print(f'cadena con espacios: {cadena2}')
print(f'cadena sin espacios: {cadena2.strip()}')# eliminar espacios al inicio y al final



